let lastQuoteIndex = -1;

export const getRandomHomeQuote = (): HomeQuote => {
  if (HOME_QUOTES.length === 0) {
    return { quote: ".", author: "" };
  }

  if (HOME_QUOTES.length === 1) {
    return HOME_QUOTES[0];
  }

  let nextIndex = Math.floor(Math.random() * HOME_QUOTES.length);
  while (nextIndex === lastQuoteIndex) {
    nextIndex = Math.floor(Math.random() * HOME_QUOTES.length);
  }

  lastQuoteIndex = nextIndex;
  return HOME_QUOTES[nextIndex];
};
